# For example if a kid needds to be above 18 to access a website, you can use an if statement to check if the kid's age is above 18. If it is, you can allow them to access the website, otherwise you can deny access.

#setting the variable
age=18

#check the logic or the condition
if age >= 18:
    print("You can access the website.")
else:    print("You cannot access the website.")

